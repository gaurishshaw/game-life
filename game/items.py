def get_item(item_key: str) -> dict:
    return ITEM_CATALOG[item_key]


def get_items_by_slot(slot: str) -> list:
    return sorted(
        [{"key": k, **v} for k, v in ITEM_CATALOG.items() if v["slot"] == slot],
        key=lambda x: (x["tier"], x["gold_cost"]),
    )


def get_stat_bonuses_from_equipment(equip: dict) -> dict:
    bonuses = {"strength": 0, "constitution": 0, "intelligence": 0, "perception": 0}
    for slot in SLOT_ORDER:
        item_key = equip.get(f"{slot}_slot")
        if item_key and item_key in ITEM_CATALOG:
            item = ITEM_CATALOG[item_key]
            for stat in bonuses:
                bonuses[stat] += item.get(stat, 0)
    return bonuses
